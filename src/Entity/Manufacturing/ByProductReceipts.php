     * Indicates if fractions (for example 0.35) are allowed for quantities of the ByProductReceipt's item
     *
     *
     * @var null|int A single byte of data.
     */
    #[EDM\Byte]
    #[Exact\Method(HttpMethod::GET)]
    public null|int $IsFractionAllowedItem = null;

